package day_0721;

public class _13_operator3 {
    public static void main(String[] args) {
        int result =10000*10000;
        System.out.println(result);

        int x =10;
        int y=1;

        y= y+x;
        System.out.println(y);

        y +=20;
        System.out.println(y);

        y*=x;
        System.out.println(y);

        y%=x;
        System.out.println(y);


        System.out.println(5/2);
        System.out.println((float) (5/2));
        System.out.println((float)5/2);


    }
}
