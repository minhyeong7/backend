package day_0721;

public class _12_operator {
    public static void main(String[] args) {
        int num =10;
        System.out.println(num);

        num++;
        System.out.println(num);

        int age=30;
        System.out.println(age++);
        System.out.println(age);

        int abc=5;
        System.out.println(++abc);

        int x=1;
        System.out.println(x++);
        System.out.println(x);
        System.out.println(++x);

    }
}
